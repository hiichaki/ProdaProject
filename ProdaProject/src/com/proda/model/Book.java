package com.proda.model;

public class Book {

	private String url;
	
	private int pages;
	
	private int symbols;

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public int getPages() {
		return pages;
	}

	public void setPages(int pages) {
		this.pages = pages;
	}

	public int getSymbols() {
		return symbols;
	}

	public void setSymbols(int symbols) {
		this.symbols = symbols;
	}
	
}
